	private void txttcKeyPressed(java.awt.event.KeyEvent evt) {
		if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

			DatabaseModel databaseModel = new DatabaseModel();
			AccountController accountController = new AccountController();

			accountController = databaseModel.accountFind(Integer.parseInt(txttc.getText()));

			if (accountController.getStudentController().getTC() == 0) {
				lblError.setText(txttc.getText() + " not found tc");
			} else {
				lblError.setText(" ");

				txtUserName.setText(accountController.getUserName());
				txtPassword.setText(accountController.getPassword());

				txtFirstName.setText(accountController.getStudentController().getFirstName());
				txtLastName.setText(accountController.getStudentController().getLastName());
				txtAge.setText(String.valueOf(accountController.getStudentController().getAge()));
				cmbGender.setSelectedItem(accountController.getStudentController().getGender());

				txtMotherName.setText(accountController.getFamilyInfoController().getMotherName());
				txtFatherName.setText(accountController.getFamilyInfoController().getFatherName());
				txtMotherJob.setText(accountController.getFamilyInfoController().getMotherJob());
				txtFatherJob.setText(accountController.getFamilyInfoController().getFatherJob());

				txtPhone.setText(accountController.getContactController().getPhoneNumber());
				txtAddress.setText(accountController.getContactController().getAddress());
				txtMail.setText(accountController.getContactController().getMail());
				lblError.setText(" ");
			}

		}
	}

