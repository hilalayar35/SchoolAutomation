	private void btnAccountAddActionPerformed(java.awt.event.ActionEvent evt) {

		DatabaseModel databaseModel = new DatabaseModel();
		java.sql.Date sqlDate = new java.sql.Date(new java.util.Date().getTime());

		AccountController accountController = new AccountController();
		accountController.setUserName(txtUserName.getText());
		if (txtPassword.getText().equals(txtRePassword.getText())) {
			accountController.setPassword(txtPassword.getText());
		} else {
			lblError.setText("Passwor not equal");
		}
		accountController.setCreatedOn(sqlDate);
		accountController.setLastLogin(sqlDate);
		accountController.setTc(Integer.parseInt(txttc.getText()));

		AccountRoleController accountRoleController = new AccountRoleController();
		accountRoleController.setRoleID(3);
		accountRoleController.setGrantDate(sqlDate);

		StudentController studentController = new StudentController();
		studentController.setFirstName(txtFirstName.getText());
		studentController.setLastName(txtLastName.getText());
		studentController.setTC(Integer.parseInt(txttc.getText()));
		studentController.setAge(Short.parseShort(txtAge.getText()));
		studentController.setGender(cmbGender.toString().charAt(0));

		FamilyInfoController familyInfoController = new FamilyInfoController();
		familyInfoController.setMotherName(txtMotherName.getText());
		familyInfoController.setMotherJob(txtMotherJob.getText());
		familyInfoController.setFatherName(txtFatherName.getText());
		familyInfoController.setFatherJob(txtFatherJob.getText());

		ContactController contactController = new ContactController();
		contactController.setPhoneNumber(txtPhone.getText());
		contactController.setMail(txtMail.getText());
		contactController.setAddress(txtAddress.getText());

		databaseModel.accountRecord(accountController, accountRoleController, studentController, familyInfoController,
				contactController);

		if (txttc != null) {
			lblError.setText("      kişi eklendi !!!");

		}
		lblError.setText(" ");

	}

