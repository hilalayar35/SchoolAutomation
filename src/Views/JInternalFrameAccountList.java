    /**
     * To show all users data info
     */
    public void showAccount() {

        DatabaseModel databaseModel = new DatabaseModel();

        List<Object[]> result = databaseModel.accountShowAllRecord();

        String arrayHeader[] = {"Account ID", "TC", "User Name", "First Name", "Last Name", "Status"};
        DefaultTableModel table = new DefaultTableModel(arrayHeader, 0);
        for (Object[] accounts : result) {
            table.addRow(accounts);
        }
        tblAccountList.setModel(table);
        jScrollPaneAccountList.setViewportView(tblAccountList);
    }

