    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        if (Integer.parseInt(lblAccountID.getText()) > 0) {

            DefaultTableModel model = (DefaultTableModel) tblInstructorOfCoursesList.getModel();

            for (int count = 0; count < model.getRowCount(); count++) {
                
                addCourseStudentController.setAddCourseStudentId(Integer.parseInt(model.getValueAt(count, 0).toString()));
                addCourseStudentController.setMidterm(Float.parseFloat(model.getValueAt(count, 7).toString()));
                addCourseStudentController.setFinalExam(Float.parseFloat(model.getValueAt(count, 8).toString()));                
               
                addCourseStudentModel.updateCourseStudentPointRecord(addCourseStudentController);
            }
            
            JOptionPane.showMessageDialog(null, "Points Saved");
        }
                
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblInstCoursesListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblInstCoursesListMouseClicked
        try {

            int selectedRow = tblInstCoursesList.getSelectedRow();
            DefaultTableModel modelCourse = (DefaultTableModel) tblInstCoursesList.getModel();

            int cC = Integer.parseInt(modelCourse.getValueAt(selectedRow, 0).toString());
            List<Object[]> result = addCourseStudentModel.coursesOfStudentListSelected(cC);

            String arrayHeader[] = {"add_course_student_id", "account_id", "first_name", "last_name", "courses_code", "courses_name", "courses_credit", "midterm", "final"};
            DefaultTableModel table = new DefaultTableModel(arrayHeader, 0);
            for (Object[] courses : result) {
                table.addRow(courses);

            }

            tblInstructorOfCoursesList.setModel(table);
            jScrollPaneInstructorOfCoursesList.setViewportView(tblInstructorOfCoursesList);

        } catch (Exception e) {
            System.err.println(e.getStackTrace());
        }
    }//GEN-LAST:event_tblInstCoursesListMouseClicked

