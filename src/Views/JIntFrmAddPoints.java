    public void showInstructorOfCourses(int accountID) {
        try {

            List<Object[]> result = instructionOfCourseDbModel.instOfCoursesFindRecord(accountID);

            String arrayHeader[] = {"IOCID", "Courses Name", "Courses Credit", "Courses Code"};
            DefaultTableModel table = new DefaultTableModel(arrayHeader, 0);
            for (Object[] courses : result) {
                table.addRow(courses);
            }

            tblInstCoursesList.setModel(table);

            jScrollPaneInstCoursesList.setViewportView(tblInstCoursesList);

            accountController = databaseModel.accountIDFind(GlobalConstants.globalConst_account_id);

            lblAccountID.setText(String.valueOf(accountController.getAccountId()));
            txtInstructorTc.setText(accountController.getTc());
            txtInstructorFirstName.setText(accountController.getFirstName());
            txtInstructorLastName.setText(accountController.getLastName());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getStackTrace());
        }

    }

